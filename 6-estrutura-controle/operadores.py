# Operadores Aritméticos
a = 10
b = 5

soma = a + b       # Soma
subtracao = a - b  # Subtração
multiplicacao = a * b  # Multiplicação
divisao = a / b    # Divisão
resto = a % b      # Resto da divisão

# Operadores de Comparação
igual = a == b     # Igualdade
maior_que = a > b  # Maior que
menor_que = a < b  # Menor que

# Operadores Lógicos
verdadeiro = True
falso = False

and_operador = verdadeiro and falso  # 'and' lógico
or_operador = verdadeiro or falso    # 'or' lógico
not_operador = not verdadeiro        # 'not' lógico

# Operadores de Atribuição
c = 7
c += 3   # Atribuição de Adição
c -= 2   # Atribuição de Subtração
c *= 5   # Atribuição de Multiplicação
c /= 2   # Atribuição de Divisão
c %= 3   # Atribuição de Módulo

# Operador de Identidade
lista1 = [1, 2, 3]
lista2 = [1, 2, 3]

identidade = lista1 is lista2       # Verifica identidade
nao_identidade = lista1 is not lista2  # Verifica não identidade

# Operador de Associação
fruta = "maçã"

verifica = 'a' in fruta     # Verifica se 'a' está na string
nao_verifica = 'b' not in fruta  # Verifica se 'b' não está na string
